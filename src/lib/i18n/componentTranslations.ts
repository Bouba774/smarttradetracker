// Component-level translations for UI elements that aren't in the main locale files
// This provides a centralized place for component-specific translations

import { Language } from './types';

type TranslationSet = Record<string, string>;
type ComponentTranslations = Record<Language, TranslationSet>;

// Helper function to get translation for current language with English fallback
export function getComponentTranslation(
  translations: ComponentTranslations,
  language: Language
): TranslationSet {
  return translations[language] || translations.en;
}

